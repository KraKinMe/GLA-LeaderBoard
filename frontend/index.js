    const exportToCSV = () => {
        const table = document.querySelector('.min-w-full'); // Select the table
        const headers = Array.from(table.querySelectorAll('thead th'))
            .map((header) => header.textContent.trim())
            .filter((header) => header); // Extract headers and remove any empty ones
    
        const rows = Array.from(table.querySelectorAll('tbody tr')).map((row) => {
            return Array.from(row.querySelectorAll('td'))
                .map((cell) => cell.textContent.trim())
                .join(','); // Extract and join cell data for each row
        });
    
        const csvContent = [headers.join(','), ...rows].join('\n'); // Combine headers and rows into CSV format
    
        const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
        const link = document.createElement('a');
        const url = URL.createObjectURL(blob);
        link.setAttribute('href', url);
        link.setAttribute('download', 'leaderboard.csv');
        link.style.display = 'none';
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
    };
    

